# function과 Class

# **함수(Function)**와 **클래스(Class)**는 모두 코드를 재사용하기 위해 묶어두는 단위라는 점에서는 같지만, 그 목적과 구조에는 뚜렷한 차이가 있습니다.
# 비유하자면, **함수는 '동사(동작)'**이고 **클래스는 '명사(대상)'**에 가깝습니다.

# 1. 공통점 (왜 둘 다 쓰는가?)
# 코드의 재사용성: 한 번 작성해두면 필요할 때마다 반복해서 불러 쓸 수 있습니다.
# 모듈화: 복잡한 코드를 기능 단위로 쪼개어 관리하기 편하게 만듭니다.
# 유지보수: 수정이 필요할 때 해당 함수나 클래스 내부만 고치면 되므로 관리가 쉽습니다.

# 구분,함수 (Function),클래스 (Class)
# 핵심 개념,동작(Action) 중심,상태(Data) + 동작(Action) 중심
# 구성 요소,명령문의 나열,속성(변수) + 메서드(함수)
# 비유,레시피 (요리하는 방법),"주방 (요리 도구와 재료, 레시피의 집합)"
# 데이터 보관,실행이 끝나면 내부 변수는 사라짐,객체 안에 데이터가 계속 유지됨
# 사용 방식,이름()으로 바로 실행,객체 = 클래스()로 실체를 만든 후 사용

def calc(ddd):
    if ddd < 10:
        return "FREE"
    else:
        return ddd * 10

# call function
print(5, "is", calc(5), ".")
print(15, "is", calc(15), ".")


class Car:
    def __init__(self, speed):
        self.speed = speed # 속도라는 '상태'를 저장

    def drive(self):
        print(f"시속 {self.speed}km로 달립니다.")

# 내부에 30이라는 데이터를 가진 '내 차'를 생성
my_car = Car(30)
my_car.drive() # 내 차가 가진 상태를 바탕으로 동작
        